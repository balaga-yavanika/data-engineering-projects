-- LOAD RAW DATA (BRONZE LAYER)

USE SCHEMA ECOMMERCE_DB.B_RAW;

CREATE STAGE IF NOT EXISTS RAW_STAGE
COMMENT = 'INTERNAL STAGE FOR OLIST E-COMMERCE CSV FILES UPLOAD';

CREATE FILE FORMAT IF NOT EXISTS OLIST_CSV_FMT
TYPE = 'CSV'
FIELD_DELIMITER = ','
RECORD_DELIMITER ='\N'
SKIP_HEADER = 1
FIELD_OPTIONALLY_ENCLOSED_BY = '"'
NULL_IF = ('NULL','NULL','','NA')
EMPTY_FIELD_AS_NULL = TRUE
DATE_FORMAT = 'AUTO'
TIMESTAMP_FORMAT = 'AUTO';

-- CREATE AND LOAD ORDERS TABLE
CREATE TABLE IF NOT EXISTS ECOMMERCE_DB.B_RAW.ORDERS(
    ORDER_ID  VARCHAR,
    CUSTOMER_ID VARCHAR,
    ORDER_STATUS VARCHAR,
    ORDER_PURCHASE_TIMESTAMP VARCHAR,
    ORDER_APPROVED_AT VARCHAR,
    ORDER_DELIVERED_CARRIER_DATE VARCHAR,
    ORDER_DELIVERED_CUSTOMER_DATE VARCHAR,
    ORDER_ESTIMATED_DELIVERY_DATE VARCHAR
);

COPY INTO ECOMMERCE_DB.B_RAW.ORDERS
FROM @ECOMMERCE_DB.B_RAW.RAW_STAGE/OLIST_ORDERS_DATASET.CSV.GZ
FILE_FORMAT = (FORMAT_NAME = 'ECOMMERCE_DB.B_RAW.OLIST_CSV_FMT')
ON_ERROR = 'ABORT_STATEMENT';

-- VERIFY THE LOAD
SELECT COUNT(*) FROM ECOMMERCE_DB.B_RAW.ORDERS;

-- CREATE AND LOAD ORDER ITEMS
