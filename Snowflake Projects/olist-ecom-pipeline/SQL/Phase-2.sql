-- CATEGORY TRANSLATIONS
CREATE TABLE IF NOT EXISTS ECOMMERCE_DB.B_RAW.PRODUCT_CATEGORY_TRANSLATIONS (
  PRODUCT_CATEGORY_NAME         VARCHAR,
  PRODUCT_CATEGORY_NAME_ENGLISH VARCHAR
);
COPY INTO ECOMMERCE_DB.B_RAW.PRODUCT_CATEGORY_TRANSLATIONS
  FROM @ECOMMERCE_DB.B_RAW.RAW_STAGE/OLIST_PRODUCT_CATEGORY_NAME_TRANSLATION.CSV.GZ
  FILE_FORMAT = (FORMAT_NAME = 'ECOMMERCE_DB.B_RAW.OLIST_CSV_FMT') ON_ERROR='ABORT_STATEMENT';



SELECT 'ORDERS' AS TBL, 
COUNT(*) AS ORDER_ROWS 
FROM ECOMMERCE_DB.B_RAW.ORDERS
UNION ALL
SELECT 'ORDER_ITEMS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.ORDER_ITEMS
UNION ALL
SELECT 'CUSTOMERS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.CUSTOMERS
UNION ALL
SELECT 'PRODUCTS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.PRODUCTS
UNION ALL
SELECT 'SELLERS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.SELLERS
UNION ALL
SELECT 'ORDER_PAYMENTS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.ORDER_PAYMENTS
UNION ALL
SELECT 'ORDER_REVIEWS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.ORDER_REVIEWS
UNION ALL
SELECT 'CATEGORY_TRANSLATIONS', COUNT(*) FROM ECOMMERCE_DB.B_RAW.PRODUCT_CATEGORY_TRANSLATIONS
ORDER BY ORDER_ROWS DESC;
